	MOV P2,#0FFH
	MOV TMOD,#20H
	MOV TH1,#0FAH
	MOV SCON,#50H
	SETB TR1
	MOV DPTR,#MYDATA
	H_1:	CLR A
			MOVC A,@A+DPTR
			JZ B_1
			ACALL SEND
			INC DPTR
			SJMP H_1
	B_1:	MOV A,P2
			ACALL SEND
			ACALL RECV
			MOV P1,A
			SJMP B_1
	
